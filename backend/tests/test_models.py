# ---------------------------------------------------------------------------

async def test_cascade_delete_menu(db_session):
    """Deleting a Menu must cascade-delete its Categories and Subcategories."""
    restaurant = _make_restaurant()
    db_session.add(restaurant)
    await db_session.flush()

    menu = _make_menu(restaurant_id=restaurant.id)
    db_session.add(menu)
    await db_session.flush()

    category = _make_category(menu_id=menu.id)
    db_session.add(category)
    await db_session.flush()

    subcategory = Subcategory(
        name="Test Subcategory",
        category_id=category.id,
    )
    db_session.add(subcategory)
    await db_session.flush()

    menu_id = menu.id
    category_id = category.id
    subcategory_id = subcategory.id

    await db_session.delete(menu)
    await db_session.flush()

    # Category must be gone
    cat_result = await db_session.execute(
        select(Category).where(Category.id == category_id)
    )
    assert cat_result.scalar_one_or_none() is None, (
        "Category must be deleted when its parent Menu is deleted (cascade)"
    )

    # Subcategory must be gone
    sub_result = await db_session.execute(
        select(Subcategory).where(Subcategory.id == subcategory_id)
    )
    assert sub_result.scalar_one_or_none() is None, (
        "Subcategory must be deleted when its parent Category is deleted (cascade)"
    )
