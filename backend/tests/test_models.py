async def test_item_price_is_decimal(db_session):
    """Item.price must round-trip as Decimal, not float."""
    restaurant = _make_restaurant()
    db_session.add(restaurant)
    await db_session.flush()

    menu = _make_menu(restaurant_id=restaurant.id)
    db_session.add(menu)
    await db_session.flush()

    category = _make_category(menu_id=menu.id)
    db_session.add(category)
    await db_session.flush()

    subcategory = Subcategory(name="Test Sub", category_id=category.id)
    db_session.add(subcategory)
    await db_session.flush()

    item = _make_item(subcategory_id=subcategory.id, price=Decimal("12.50"))
    db_session.add(item)
    await db_session.flush()

    result = await db_session.execute(select(Item).where(Item.id == item.id))
    fetched = result.scalar_one()
    assert isinstance(fetched.price, Decimal), (
        f"Item.price must be Decimal after round-trip, got {type(fetched.price)}"
    )
    assert fetched.price == Decimal("12.50")


async def test_item_price_not_float(db_session):
    """Item.price column type must be Numeric(10,2), not Float."""
    from sqlalchemy import inspect as sa_inspect
    from sqlalchemy import Numeric

    # Inspect the Item table column type
    mapper = Item.__mapper__
    price_col = mapper.columns["price"]
    assert isinstance(price_col.type, Numeric), (
        f"Item.price must be Numeric type, got {type(price_col.type)}"
    )
    # Numeric subclass check — must NOT be Float
    from sqlalchemy import Float
    assert not isinstance(price_col.type, Float), (
        "Item.price must NOT be Float — use Numeric(10, 2)"
    )


